def area_rectangle(l: float,w: float):
    """Calculates the area of a rectangle with the formula l*w"""
    return l*w

def perimeter_rectangle(l:float, w:float):
    """Calculates the perimeter of a rectangle with the formula l*2 + w*2"""
    return l*2+w*2

def area_triangle(b:float, h:float):
    """Calculates the area of a triangle with the formula b*h"""
    return .5*b*h

def perimeter_triangle(a:float, b:float, c:float):
    """Calculates the perimeter of a triangle with the formula a+b+c"""
    return a+b+c